// Take input for the first variable
let firstInput = prompt("Enter the first number:");

// Multiply the first input by 2
let multipliedResult = parseFloat(firstInput) * 2;

// Take input for the second variable
let secondInput = prompt("Enter the second number:");

// Add the second input to the result of the first input multiplied by 2
let finalResult = multipliedResult + parseFloat(secondInput);

// Display the final result using alert()
alert("The final result is: " + finalResult);
